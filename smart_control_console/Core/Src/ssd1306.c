static void SSD1306_WriteChar(char c)
{
    // Convert to font array index (ASCII 32-126)
    if (c < 32 || c > 126)
    {
        c = 32; // Default to space for unsupported characters
    }
    
    const uint8_t *bitmap = font5x7[c - 32];

    for (uint8_t col = 0; col < 5; col++)
    {
        for (uint8_t row = 0; row < 8; row++)
        {
            if (bitmap[col] & (1 << row))
                SSD1306_DrawPixel(cursor_x + col, cursor_y + row, 1);
        }
    }

    cursor_x += 6; // 5 pixels + 1 pixel spacing
}

void SSD1306_WriteString(char *str)
{
    while (*str)
    {
        SSD1306_WriteChar(*str++);
    }
}
